Input: 
Employee table:
+----+-------+--------+-----------+
| id | name  | salary | managerId |
+----+-------+--------+-----------+
| 1  | Joe   | 70000  | 3         |
| 2  | Henry | 80000  | 4         |
| 3  | Sam   | 60000  | Null      |
| 4  | Max   | 90000  | Null      |
+----+-------+--------+-----------+
Output: 
+----------+
| Employee |
+----------+
| Joe      |
+----------+
Explanation: Joe is the only employee who earns more than his manager.

-- Write an SQL query to find the employees who earn more than their managers.Return the result table in any order.

SELECT tb1.name AS Employee
FROM Employee tb1, Employee tb2
WHERE tb1.managerId = tb2.id AND tb1.salary > tb2.salary;